#!/usr/bin/env python3

# รับอินพุตตัวเลขสองตัวจากผู้ใช้
num1 = float(input("Give me the first number: "))
num2 = float(input("Give me the second number: "))

# แสดงผลลัพธ์ของการดำเนินการทางคณิตศาสตร์
print("Thank you!")
print(num1, "+", num2, "=", num1 + num2)
print(num1, "-", num2, "=", num1 - num2)
print(num1, "/", num2, "=", num1 / num2)
print(num1, "*", num2, "=", num1 * num2)